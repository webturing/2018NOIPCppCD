#include<iostream>
using namespace std;
int main()
{
	cout<<"4>3 = "<<(4>3)<<endl;
	cout<<"4<3 = "<<(4<3)<<endl;
	cout<<"4>=3 = "<<(4>=3)<<endl;
	cout<<"4<=3 = "<<(4<=3)<<endl;
	cout<<"4==3 = "<<(4==3)<<endl;
	cout<<"4!=3 = "<<(4!=3)<<endl;
	
	cout<<"5>4+2 = "<<(5>4+2)<<endl;
	cout<<"5>4>3 = "<<(5>4>3)<<endl;
	return 0;
}
